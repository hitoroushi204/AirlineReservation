    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            String flightNo = txtFlightNo.getText().trim();
            String origin = txtFrom.getText().trim();
            String dest = txtTo.getText().trim();
            String date = txtDate.getText().trim();
            String departureTime = txtDeparture.getText().trim();

            int ecoSeats = Integer.parseInt(txtEcoSeats.getText().trim());
            int busSeats = Integer.parseInt(txtBusSeats.getText().trim());
            double ecoPrice = Double.parseDouble(txtEcoPrice.getText().trim());
            double busPrice = Double.parseDouble(txtBusPrice.getText().trim());

            if (origin.isEmpty() || dest.isEmpty() || date.isEmpty() || departureTime.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please fill all required fields.");
                return;
            }

            String query = "INSERT INTO flights (flight_no, origin, destination, departure_date, departure_time, "
                    + "economy_seats, business_seats, economy_price, business_price) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
            try (Connection conn = DBConnection.getConnection(); PreparedStatement stmt = conn.prepareStatement(query)) {
                stmt.setString(1, flightNo);
                stmt.setString(2, origin);
                stmt.setString(3, dest);
                stmt.setString(4, date);
                stmt.setString(5, departureTime);
                stmt.setInt(6, ecoSeats);
                stmt.setInt(7, busSeats);
                stmt.setDouble(8, ecoPrice);
                stmt.setDouble(9, busPrice);
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Flight added successfully.");
                loadFlights(); // Refresh table
                clearForm();
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Invalid number format. Please check seats and prices.");
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int selectedRow = tblFlights.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a flight to update.");
            return;
        }

        try {
            String flightNo = txtFlightNo.getText().trim();
            int flightId = (int) tblFlights.getValueAt(selectedRow, 0);
            String origin = txtFrom.getText().trim();
            String dest = txtTo.getText().trim();
            String date = txtDate.getText().trim();
            String departureTime = txtDeparture.getText().trim();
            
            if (txtEcoSeats.getText().trim().isEmpty()
                    || txtBusSeats.getText().trim().isEmpty()
                    || txtEcoPrice.getText().trim().isEmpty()
                    || txtBusPrice.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please fill all seat and price fields.");
                return;
            }

            int ecoSeats = Integer.parseInt(txtEcoSeats.getText().trim());
            int busSeats = Integer.parseInt(txtBusSeats.getText().trim());
            double ecoPrice = Double.parseDouble(txtEcoPrice.getText().trim());
            double busPrice = Double.parseDouble(txtBusPrice.getText().trim());

            String query = "UPDATE flights SET flight_no=?, origin=?, destination=?, departure_date=?, departure_time=?, "
                    + "economy_seats=?, business_seats=?, economy_price=?, business_price=? WHERE id=?";
            try (Connection conn = DBConnection.getConnection(); PreparedStatement stmt = conn.prepareStatement(query)) {
                stmt.setString(1, flightNo);
                stmt.setString(2, origin);
                stmt.setString(3, dest);
                stmt.setString(4, date);
                stmt.setString(5, departureTime);
                stmt.setInt(6, ecoSeats);
                stmt.setInt(7, busSeats);
                stmt.setDouble(8, ecoPrice);
                stmt.setDouble(9, busPrice);
                stmt.setInt(10, flightId);
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Flight updated successfully.");
                loadFlights();
                clearForm();
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Invalid number format. Please check seats and prices.\n" + ex.getMessage());
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tblFlights.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a flight to delete.");
            return;
        }

        int flightId = (int) tblFlights.getValueAt(selectedRow, 0);
        String flightInfo = (String) tblFlights.getValueAt(selectedRow, 1) + " - "
                + tblFlights.getValueAt(selectedRow, 2) + " to "
                + tblFlights.getValueAt(selectedRow, 3);

        int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete flight " + flightInfo + "?\nThis action cannot be undone.",
                "Confirm Delete", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

        if (confirm == JOptionPane.YES_OPTION) {
            String query = "DELETE FROM flights WHERE id=?";
            try (Connection conn = DBConnection.getConnection(); PreparedStatement stmt = conn.prepareStatement(query)) {
                stmt.setInt(1, flightId);
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Flight deleted successfully.");
                loadFlights();
                clearForm();
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

