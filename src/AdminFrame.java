    private void addPlaceholder(javax.swing.JTextField textField, String placeholder) {
        textField.setText(placeholder);
        textField.setForeground(Color.GRAY);
        
        textField.addFocusListener(new FocusAdapter() {
            @Override
            public void focusGained(FocusEvent e) {
                if (textField.getText().equals(placeholder)) {
                    textField.setText("");
                    textField.setForeground(Color.BLACK);
                }
            }
            
            @Override
            public void focusLost(FocusEvent e) {
                if (textField.getText().isEmpty()) {
                    textField.setText(placeholder);
                    textField.setForeground(Color.GRAY);
                }
            }
        });
    }

    private void loadFlights() {
        DefaultTableModel model = (DefaultTableModel) tblFlights.getModel();
        model.setRowCount(0);

        String query = "SELECT id, flight_no, origin, destination, departure_date, departure_time, "
                + "economy_seats, business_seats, economy_price, business_price FROM flights";
        try (Connection conn = DBConnection.getConnection(); Statement stmt = conn.createStatement(); ResultSet rs = stmt.executeQuery(query)) {
            while (rs.next()) {
                Object[] row = {
                    rs.getInt("id"),
                    rs.getString("flight_no"),
                    rs.getString("origin"),
                    rs.getString("destination"),
                    rs.getString("departure_date"),
                    rs.getString("departure_time"),
                    rs.getInt("economy_seats"),
                    rs.getInt("business_seats"),
                    String.format("P%.2f", rs.getDouble("economy_price")),
                    String.format("P%.2f", rs.getDouble("business_price"))
                };
                model.addRow(row);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading flights: " + ex.getMessage());
        }
    }

    private String calculateArrivalTime(String departureTime) {
        try {
            String[] parts = departureTime.split(":");
            int hour = Integer.parseInt(parts[0]);
            int minute = Integer.parseInt(parts[1]);

            hour += 1;
            minute += 30;

            if (minute >= 60) {
                minute -= 60;
                hour++;
            }

            if (hour >= 24) {
                hour -= 24;
            }

            return String.format("%02d:%02d", hour, minute);
        } catch (Exception e) {
            return "N/A";
        }
    }
    
